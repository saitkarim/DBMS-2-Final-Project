CREATE TABLE new_laptops(
    BRAND           VARCHAR2(26 BYTE),
    LAPTOP_NAME VARCHAR2(128 BYTE),
    DISPLAY_SIZE VARCHAR2(26 BYTE),
    PROCESSOR_TYPE VARCHAR2(50 BYTE),
    GRAPHICS_CARD VARCHAR2(128 BYTE),
    DISK_SPACE VARCHAR2(128 BYTE),
    UPDATED_DATE DATE
);
SELECT * from new_laptops;
SELECT * from deleted_laptops;

DROP TABLE deleted_laptops;
CREATE TABLE deleted_laptops(
    BRAND           VARCHAR2(26 BYTE),
    LAPTOP_NAME VARCHAR2(128 BYTE),
    DISPLAY_SIZE VARCHAR2(26 BYTE),
    PROCESSOR_TYPE VARCHAR2(50 BYTE),
    GRAPHICS_CARD VARCHAR2(128 BYTE),
    DISK_SPACE VARCHAR2(128 BYTE),
    DELETED_DATE DATE
);

CREATE TABLE updated_laptops(
    BRAND           VARCHAR2(26 BYTE),
